const TRANSLATIONS = { es: ES, en: EN }

const LangCtx = createContext(null)

export function LanguageProvider({ children }) {
  const [lang, setLangState] = useState(() => localStorage.getItem('mary_lang') || 'es')

  const setLang = (l) => {
    setLangState(l)
    localStorage.setItem('mary_lang', l)
  }

  return <LangCtx.Provider value={{ lang, setLang }}>{children}</LangCtx.Provider>
}

export function useLanguage() {
  return useContext(LangCtx)
}

export function useT() {
  const { lang } = useContext(LangCtx)
  const dict = TRANSLATIONS[lang] || TRANSLATIONS.es
  return (key, vars) => {
    let str = dict[key] || TRANSLATIONS.es[key] || key
    if (vars) Object.entries(vars).forEach(([k, v]) => { str = str.replace(`{${k}}`, v) })
    return str
  }
}

export const MONEDAS = ['USD', 'NIO', 'COP', 'GTQ', 'PEN', 'MXN']

export function getStatusLabel(estado, lang) {
  const key = 'status_' + estado
  const dict = TRANSLATIONS[lang] || TRANSLATIONS.es
  return dict[key] || estado
}
