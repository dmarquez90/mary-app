export const LangContext = createContext()

export function LangProvider({ children }) {
  const [lang, setLang] = useState('ES')

  useEffect(() => {
    // Load from localStorage first for instant render
    const saved = localStorage.getItem(LS_KEY)
    if (saved) setLang(saved)
    // Then sync from Supabase if user is logged in
    supabase.auth.getUser().then(({ data: { user } }) => {
      if (!user) return
      supabase.from('usuarios').select('lang').eq('id', user.id).single()
        .then(({ data }) => {
          if (data?.lang) {
            setLang(data.lang)
            localStorage.setItem(LS_KEY, data.lang)
          }
        })
    })
  }, [])

  const toggleLang = () => {
    const newLang = lang === 'ES' ? 'EN' : 'ES'
    setLang(newLang)
    localStorage.setItem(LS_KEY, newLang)
    // Persist to Supabase
    supabase.auth.getUser().then(({ data: { user } }) => {
      if (user) supabase.from('usuarios').update({ lang: newLang }).eq('id', user.id)
    })
  }

  const t = (key, vars = {}) => {
    const dict = lang === 'ES' ? ES : EN
    let text = dict[key] ?? (lang === 'ES' ? ES[key] : EN[key]) ?? key
    Object.keys(vars).forEach(v => {
      text = text.replace(`{${v}}`, vars[v])
    })
    return text
  }

  return (
    <LangContext.Provider value={{ lang, toggleLang, t }}>
      {children}
    </LangContext.Provider>
  )
}

export const useLang          = () => useContext(LangContext)
export const useT             = () => useContext(LangContext).t
export const LanguageProvider = LangProvider
export const useLanguage      = useLang
