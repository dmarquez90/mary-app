export function LangProvider({ children }) {
  const [lang, setLang] = useState(() => localStorage.getItem('mary_lang') || 'en')

  function toggleLang() {
    const next = lang === 'en' ? 'es' : 'en'
    setLang(next)
    localStorage.setItem('mary_lang', next)
  }

  function t(key, params) {
    const dict = translations[lang] || translations["en"]
    let str = dict[key] || translations["en"][key] || key
    if (params && typeof str === 'string') {
      Object.entries(params).forEach(([k, v]) => {
        str = str.replace(new RegExp(`\{${k}\}`, 'g'), v)
      })
    }
    return str
  }

  return (
    <LangContext.Provider value={{ lang, toggleLang, t }}>
      {children}
    </LangContext.Provider>
  )
}

export function useLang() {
  return useContext(LangContext)
}

// Alias de LangProvider usado en main.jsx
export const LanguageProvider = LangProvider

// Alias para componentes que solo necesitan la función t()
export function useT() {
  const { t } = useContext(LangContext)
  return t
}

